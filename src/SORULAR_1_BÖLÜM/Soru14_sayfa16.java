public class Soru14_sayfa16 {
    public static void main(String[] args) {

        //Factorial number **** faktöriyel numarası


        //5!=5*4*3*2*1=120

        int a=5;
        int fac=1;

        for (int i=1; i<=a; i++){
            fac=fac*i;
        }
        System.out.println("fac = " + fac);


    }
}
